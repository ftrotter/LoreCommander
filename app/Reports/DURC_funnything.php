namespace App\Reports;
use ftrotter\Zermelo\Reports\Tabular\AbstractTabularReport;

class DURC_funnything extends AbstractTabularReport
{

    //returns the name of the report
    public function GetReportName(): string {
        $report_name = "funnything Report";
        return($report_name);
    }

    //returns the description of the report. HTML is allowed here.
    public function GetReportDescription(): ?string {
        $desc = "View the funnything data
			<br>
			<a href='/DURC/funnything/create'>Add new funnything</a>
";
        return($desc);
    }

    //  returns the SQL for the report.  This is the workhorse of the report.
    public function GetSQL()
    {

        $is_debug = false; //lots of debugging echos will be show instead of the report

        $index = $this->getCode();


	//get the local image field for this report... null if not found..
	$img_field_name = \App\funnything::getImgField();
	if(isset($$img_field_name)){
		$img_field = $$img_field_name;
	}else{
		$img_field = null;
	}

	$joined_select_field_sql  = '';




        if(is_null($index)){

                $sql = "
SELECT
funnything.id
$joined_select_field_sql 
, funnything.thisint AS thisint
, funnything.thisfloat AS thisfloat
, funnything.thisdecimal AS thisdecimal
, funnything.thisvarchar AS thisvarchar
, funnything.thisdate AS thisdate
, funnything.thisdatetime AS thisdatetime
, funnything.thistimestamp AS thistimestamp
, funnything.thischar AS thischar
, funnything.thistext AS thistext
, funnything.thisblob AS thisblob
, funnything.thistinyint AS thistinyint
, funnything.thistinytext AS thistinytext

FROM DURC_aaa.funnything

";

        }else{

                $sql = "
SELECT
funnything.id 
$joined_select_field_sql
, funnything.thisint AS thisint
, funnything.thisfloat AS thisfloat
, funnything.thisdecimal AS thisdecimal
, funnything.thisvarchar AS thisvarchar
, funnything.thisdate AS thisdate
, funnything.thisdatetime AS thisdatetime
, funnything.thistimestamp AS thistimestamp
, funnything.thischar AS thischar
, funnything.thistext AS thistext
, funnything.thisblob AS thisblob
, funnything.thistinyint AS thistinyint
, funnything.thistinytext AS thistinytext
 
FROM DURC_aaa.funnything 

WHERE funnything.id = $index
";

        }

        if($is_debug){
                echo "<pre>$sql";
                exit();
        }

        return $sql;
    }

    //decorate the results of the query with useful results
    public function MapRow(array $row, int $row_number) :array
    {

	$is_debug = false;
	
	//we think it is safe to extract here because we are getting this from the DB and not a user directly..
        extract($row);


	//get the local image field for this report... null if not found..
	$img_field_name = \App\funnything::getImgField();
	if(isset($$img_field_name)){
		$img_field = $$img_field_name;
	}else{
		$img_field = null;
	}

	$joined_select_field_sql  = '';





        //link this row to its DURC editor
        $row['id'] = "<a href='/DURC/funnything/$id'>$id</a>";



	if(isset($$img_field_name)){  //is it set
		if(strlen($img_field) > 0){ //and it is it really a url..
			$row[$img_field_name] = "<img width='300' src='$img_field'>";
		}
	}





        return $row;
    }

    //see Zermelo documentation to understand following functions:
    //https://github.com/CareSet/Zermelo

    public $NUMBER     = ['ROWS','AVG','LENGTH','DATA_FREE'];
    public $CURRENCY = [];
    public $SUGGEST_NO_SUMMARY = ['ID'];
    public $REPORT_VIEW = null;

    public function OverrideHeader(array &$format, array &$tags): void
    {
    }

    public function GetIndexSQL(): ?array {
                return(null);
    }

        //turns on the cache, should be off for development and small databases or simple queries
   public function isCacheEnabled(){
        return(false);
   }

        //only matters if the cache is on
   public function howLongToCacheInSeconds(){
        return(1200); //twenty minutes by default
   }

}

