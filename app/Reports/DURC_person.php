namespace App\Reports;
use ftrotter\Zermelo\Reports\Tabular\AbstractTabularReport;

class DURC_person extends AbstractTabularReport
{

    //returns the name of the report
    public function GetReportName(): string {
        $report_name = "person Report";
        return($report_name);
    }

    //returns the description of the report. HTML is allowed here.
    public function GetReportDescription(): ?string {
        $desc = "View the person data
			<br>
			<a href='/DURC/person/create'>Add new person</a>
";
        return($desc);
    }

    //  returns the SQL for the report.  This is the workhorse of the report.
    public function GetSQL()
    {

        $is_debug = false; //lots of debugging echos will be show instead of the report

        $index = $this->getCode();


	//get the local image field for this report... null if not found..
	$img_field_name = \App\person::getImgField();
	if(isset($$img_field_name)){
		$img_field = $$img_field_name;
	}else{
		$img_field = null;
	}

	$joined_select_field_sql  = '';




        if(is_null($index)){

                $sql = "
SELECT
person.id
$joined_select_field_sql 
, person.last_name AS last_name
, person.first_name AS first_name
, person.person_blurb AS person_blurb
, person.image_uri AS image_uri
, person.wallpaper_download_url AS wallpaper_download_url
, person.mtgwiki_url AS mtgwiki_url
, person.wizards_story_url AS wizards_story_url
, person.created_at AS created_at
, person.updated_at AS updated_at

FROM lore.person

";

        }else{

                $sql = "
SELECT
person.id 
$joined_select_field_sql
, person.last_name AS last_name
, person.first_name AS first_name
, person.person_blurb AS person_blurb
, person.image_uri AS image_uri
, person.wallpaper_download_url AS wallpaper_download_url
, person.mtgwiki_url AS mtgwiki_url
, person.wizards_story_url AS wizards_story_url
, person.created_at AS created_at
, person.updated_at AS updated_at
 
FROM lore.person 

WHERE person.id = $index
";

        }

        if($is_debug){
                echo "<pre>$sql";
                exit();
        }

        return $sql;
    }

    //decorate the results of the query with useful results
    public function MapRow(array $row, int $row_number) :array
    {

	$is_debug = false;
	
	//we think it is safe to extract here because we are getting this from the DB and not a user directly..
        extract($row);


	//get the local image field for this report... null if not found..
	$img_field_name = \App\person::getImgField();
	if(isset($$img_field_name)){
		$img_field = $$img_field_name;
	}else{
		$img_field = null;
	}

	$joined_select_field_sql  = '';





        //link this row to its DURC editor
        $row['id'] = "<a href='/DURC/person/$id'>$id</a>";



	if(isset($$img_field_name)){  //is it set
		if(strlen($img_field) > 0){ //and it is it really a url..
			$row[$img_field_name] = "<img width='300' src='$img_field'>";
		}
	}





        return $row;
    }

